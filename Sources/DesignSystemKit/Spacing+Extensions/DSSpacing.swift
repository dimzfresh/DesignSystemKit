import Foundation

public enum DSSpacing: CGFloat {
    /// Zero spacing (0 points)
    case zero = 0
    /// Extra extra small spacing (2 points)
    case xxs = 2
    /// Extra small spacing (4 points)
    case xs = 4
    /// Small spacing (8 points)
    case s = 8
    /// Medium spacing (12 points)
    case m = 12
    /// Large spacing (16 points)
    case l = 16
    /// Extra large spacing (20 points)
    case xl = 20
    /// Extra extra large spacing (24 points)
    case xxl = 24
    /// Triple extra large spacing (28 points)
    case xxxl = 28
    /// Quadruple extra large spacing (32 points)
    case xxxxl = 32
}
